package ygq.study.javase.lambda.stream.bean;

public enum SexEnum {

	MALE("M", "男"), 
	FEMALE("F", "女");

	private SexEnum(String code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	private String code;
	private String desc;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
